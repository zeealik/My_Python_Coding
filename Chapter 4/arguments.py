def greet(first_name, last_name):  # these are parameters
    print(f"Hi {first_name} {last_name}")  # built in function
    print("Welcome")


# user defined function
greet("Zeeshan", "Ali Khan")
# "Zeeshan" and "Ali Khan" are arguments

greet("Bilal", "Jani")
